package day07_Operators;

public class Variables {

    public static void main(String[] args) {

        // a = 10, b = 15, output a = 15 b = 10

        int a = 10;
        a += 5;

        int b = 15;

        b -= 5;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        


    }
}
